  );

  CREATE INDEX "IWP_DOCUMENTS_FK9" ON "IWP_DOCUMENTS" ("OFFENDER_BOOK_ID");


  CREATE UNIQUE INDEX "IWP_DOCUMENTS_PK" ON "IWP_DOCUMENTS" ("DOCUMENT_ID");
