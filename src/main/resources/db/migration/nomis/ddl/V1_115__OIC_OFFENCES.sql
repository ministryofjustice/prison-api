CREATE TABLE OIC_OFFENCES (
    OIC_OFFENCE_CODE VARCHAR2(12),
    DESCRIPTION VARCHAR2(240),
    ACTIVE_FLAG VARCHAR2(1) DEFAULT 'Y' ,
    LIST_SEQ NUMBER(6,0),
    UPDATE_ALLOWED_FLAG VARCHAR2(1) DEFAULT 'Y' ,
    EXPIRY_DATE DATE,
    MODIFY_USER_ID VARCHAR2(32),
    OIC_OFFENCE_CATEGORY VARCHAR2(12),
    OIC_OFFENCE_TYPE VARCHAR2(12),
    MAX_PENALTY_MONTHS NUMBER(3,0),
    MAX_PENALTY_DAYS NUMBER(3,0),
    CREATE_DATETIME TIMESTAMP DEFAULT systimestamp ,
    CREATE_USER_ID VARCHAR2(32) DEFAULT USER ,
    MODIFY_DATETIME TIMESTAMP,
    AUDIT_TIMESTAMP TIMESTAMP,
    AUDIT_USER_ID VARCHAR2(32),
    AUDIT_MODULE_NAME VARCHAR2(65),
    AUDIT_CLIENT_USER_ID VARCHAR2(64),
    AUDIT_CLIENT_IP_ADDRESS VARCHAR2(39),
    AUDIT_CLIENT_WORKSTATION_NAME VARCHAR2(64),
    AUDIT_ADDITIONAL_INFO VARCHAR2(256),
    START_DATE DATE,
    END_DATE DATE,
    OIC_OFFENCE_ID NUMBER(10,0),
    CONSTRAINT OIC_OFFENCES_PK PRIMARY KEY (OIC_OFFENCE_ID),
    CONSTRAINT OIC_OFFENCES_UK1 UNIQUE (OIC_OFFENCE_CODE,START_DATE)
);
CREATE UNIQUE INDEX OIC_OFFENCES_PK ON OIC_OFFENCES (OIC_OFFENCE_ID);
CREATE UNIQUE INDEX OIC_OFFENCES_UK1 ON OIC_OFFENCES (OIC_OFFENCE_CODE,START_DATE);
