  fun toPrisonerDetail() = PrisonerDetail(
    this.offenderNo,
    this.title,
    this.suffix,
    this.firstName,
    this.middleNames,
    this.lastName,
    this.dateOfBirth,
    this.gender,
    this.sexCode,
    this.nationalities,
    this.currentlyInPrison,
    this.latestBookingId,
    this.latestLocationId,
    this.latestLocation,
    this.internalLocation,
    this.pncNumber,
    this.croNumber,
    this.ethnicity,
    this.ethnicityCode,
    this.birthCountry,
    this.religion,
    this.religionCode,
    this.convictedStatus,
    this.legalStatus,
    this.bandCode,
    this.imprisonmentStatus,
    this.imprisonmentStatusDesc,
    this.receptionDate,
    this.maritalStatus,
    this.currentWorkingFirstName,
    this.currentWorkingLastName,
    this.currentWorkingBirthDate,
  )
}
